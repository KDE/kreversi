#include <config.h>

#include "Random.h"
#include <stdlib.h>
#include <sys/times.h>

Random::Random() { 
  clock_t t;

  struct tms dummy;
  t = times(&dummy);
  setSeed((int)t);
}

Random::Random(int seed) { 
  setSeed(seed); 
};

void Random::setSeed(int seed) { 
  srandom((unsigned int)seed); 
}

int Random::nextInt() {
  int r = random();
  return r; 
}
